places=['Britain', 'Dubai', 'America', 'Malawi', 'China']
print("original list")
print(places)
print("")
print(sorted(places))
print("showing list is still in original order")
print(places)
print("using sorted to show the list is reversed")
print(sorted(places,reverse=True))
print(places)
print("reversing the list")
places.reverse()
print(places)
print("reversing the list again")
places.reverse()
print(places)
print("using sort to change the oder of my list")
places.sort()
print(places)
print("using sort reverse to change the other of the list")
places.sort(reverse=True)
print(places)









